package com.mohamdy.blind75.linkedlist;

// Standard LeetCode singly-linked list node (provided scaffolding, not a solution).
public class ListNode {
    public int val;
    public ListNode next;

    public ListNode() {
    }

    public ListNode(int val) {
        this.val = val;
    }

    public ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}
